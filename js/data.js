// 캐릭터 파일 목록 생성 (Student만, NPC 제외, _Small 제외)
function getCharacterList() {
  const list = [];
  for (const [key, korName] of Object.entries(CHARACTER_DATA)) {
    list.push({
      id: key,
      file: `${key}.png`,
      nameKo: korName,
      nameEn: key.replace(/_/g, ' ')
    });
  }
  return list.sort((a, b) => a.nameKo.localeCompare(b.nameKo, 'ko'));
}
